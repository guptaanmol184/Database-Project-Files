    private void view_BtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_view_BtnActionPerformed
        // TODO add your handling code here:
        DefaultTableModel rec_model=(DefaultTableModel) recordTable.getModel();
        
        int rowCount = rec_model.getRowCount();
        //Remove rows one by one from the end of the table
        for (int i = rowCount - 1; i >= 0; i--) {
            rec_model.removeRow(i);
}

        try
        {
            Class.forName("java.sql.DriverManager");
            
            String host="localhost";
            String port="3306";
            String dbname="COMPLAINTS";
            String username="complaints";
            String password="iiitdm123";
            
            String dburl="jdbc:mysql://"+host+":"+port+"/"+dbname;
            
            Connection con = (Connection) DriverManager.getConnection(dburl, username,password);
            Statement stmt = (Statement) con.createStatement();
            
            String base_query="SELECT type, roll_no, c_roomno, c_hostel, description from private_complaints where";
            
            String parameters="";
            
            String type=(String) typeCB.getSelectedItem();
            if(type.equals("ANY"))
                type="%";
            parameters=parameters.concat("(type like '"+type+"')");
            
            String hostel=(String) hostelCB.getSelectedItem();
            if(hostel.equals("ANY"))
                hostel="%";
            parameters=parameters.concat(" and (c_hostel like '"+hostel+"')");
            
            if(date_CB.isSelected()==false)
            {
                String start_date=start_dateTxt.getText();
                String end_date=end_dateTxt.getText();
                
                parameters=parameters.concat(" and (create_date between '"+start_date+"' and '"+end_date+"')");
            }
            
            if(redressedCB.isSelected()==true)
                parameters=parameters.concat(" and status='REDRESSED'");
            
            else if(unredressedCB.isSelected()==true)
                parameters=parameters.concat(" and status!='REDRESSED'");
                
            
            if(floor_CB.isSelected()==false)
            {
                int floor=Integer.parseInt(floorTxt.getText());
                parameters=parameters.concat(" and TRUNCATE(c_roomno/100,0)-1="+floor);
            }
            
            String query=base_query+" "+parameters;
            ResultSet rs=stmt.executeQuery(query);
            
            String rs_type,rs_rollno,rs_hostel,rs_description;
            int rs_roomno;
            
            while(rs.next())
            {
                rs_type=rs.getString("type");
                rs_rollno=rs.getString("roll_no");
                rs_roomno=rs.getInt("c_roomno");
                rs_hostel=rs.getString("c_hostel");
                rs_description=rs.getString("description");
                
                rec_model.addRow(new Object[] {rs_type,rs_rollno,rs_roomno,rs_hostel,rs_description});
            }
            
            rs.close();
            stmt.close();
            con.close();
        }
        
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_view_BtnActionPerformed

