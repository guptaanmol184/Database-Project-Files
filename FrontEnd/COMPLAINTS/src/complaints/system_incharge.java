    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        //Summarise the pending private complaints
        try
        {
            Class.forName("java.sql.DriverManager");
            
            String host="localhost";
            String port="3306";
            String dbname="COMPLAINTS";
            String username="complaints";
            String password="iiitdm123";
            
            String dburl="jdbc:mysql://"+host+":"+port+"/"+dbname;
            
            Connection con = (Connection) DriverManager.getConnection(dburl, username,password);
            Statement stmt = (Statement) con.createStatement();
            
            String comp_dept;
            int count;
            
            //get unredressed complaints from private_complaints
            String query="Select type, count(*) CNT from private_complaints where status!='REDRESSED' group by type";
            
            ResultSet rs=stmt.executeQuery(query);
            
            while(rs.next())
            {
                comp_dept=rs.getString("type");
                count=rs.getInt("CNT");
                
                if(comp_dept.equals("CIVIL"))
                {
                    civil_cntLbl.setText(""+count);
                    if(count>10)
                        civil_cntLbl.setForeground(Color.red);
                }
                
                else if(comp_dept.equals("ELECTRICAL"))
                {
                    ele_cntLbl.setText(""+count);
                    if(count>10)
                        ele_cntLbl.setForeground(Color.red);
                }
                
                else if(comp_dept.equals("NETWORK"))
                {
                    net_cntLbl.setText(""+count);
                    if(count>10)
                        net_cntLbl.setForeground(Color.red);
                }
                
                else if(comp_dept.equals("GENERAL"))
                {
                    gen_cntLbl.setText(""+count);
                    if(count>10)
                        gen_cntLbl.setForeground(Color.red);
                }
            }
            
            rs.close();
            
            //All private complaints  registered this week
            query="Select type, count(*) CNT from private_complaints where (YEAR(CURRENT_DATE)-YEAR(create_date))<=1 and WEEK(CURRENT_DATE,2)=WEEK(create_date,2) group by type";
           
            ResultSet rs_week=stmt.executeQuery(query);
            while(rs_week.next())
            {
                comp_dept=rs_week.getString("type");
                count=rs_week.getInt("CNT");
                
                if(comp_dept.equals("CIVIL"))
                {
                    civilwk_cntLbl.setText(""+count);
                    if(count>10)
                        civilwk_cntLbl.setForeground(Color.red);
                }
                
                else if(comp_dept.equals("ELECTRICAL"))
                {
                    elewk_cntLbl.setText(""+count);
                    if(count>10)
                        elewk_cntLbl.setForeground(Color.red);
                }
                
                else if(comp_dept.equals("NETWORK"))
                {
                    netwk_cntLbl.setText(""+count);
                    if(count>10)
                        netwk_cntLbl.setForeground(Color.red);
                }
                
                else if(comp_dept.equals("GENERAL"))
                {
                    genwk_cntLbl.setText(""+count);
                    if(count>10)
                        genwk_cntLbl.setForeground(Color.red);
                }
            }
            
            rs_week.close();
            
            
            stmt.close();
            con.close();
            
        }
        
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_formWindowOpened

